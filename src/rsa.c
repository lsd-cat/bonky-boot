bool rsa_verify(uint8_t *signature, uint8_t *hash) {
	// Allocate big integers
	bool signature_valid = true;
	numeri *sig = numeri_alloc();
	numeri *n = numeri_alloc();
	numeri *h = numeri_alloc();

	// Load the signature
	numeri_load(sig, signature, RSA_KEY_SIZE);

	// Load the modulus
	numeri_load(n, (void *)modulus, RSA_KEY_SIZE);

	// Compute h' = s^e (mod n)
	numeri_pow(sig, exponent, n, h);

	// Check computed hash
	for (size_t byte_idx = 0; byte_idx < SHA256_DIGEST_SIZE; byte_idx++) {
		if (hash[byte_idx] != h->data[SHA256_DIGEST_SIZE - (byte_idx + 1)]) {
			signature_valid = false;
			break;
		}
	}

	// Free allocated memory
	numeri_free(sig);
	numeri_free(n);
	numeri_free(h);

	return signature_valid;
}
